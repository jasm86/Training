import UIKit

extension UIColor{
    public class func progressColor() -> UIColor {
        return UIColor(red: 36.0 / 255.0, green: 189.0 / 255.0, blue: 244.0 / 255.0, alpha: 1.0)
    }
    
    public class func progressBackground() -> UIColor {
        return UIColor(red: 63.0 / 255.0, green: 63.0 / 255.0, blue: 63.0 / 255.0, alpha: 0.5)
    }
}
